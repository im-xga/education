import math

r=float(input("Введіть найкоротшу відстань від центру квадрата до його сторони: ")) 
x=float(input("Введіть координату x точки: ")) 
y=float(input("Введіть координату y точки: ")) 

if x>r or x<-r or y>r or y<-r:
  print("Точка не належить квадрату.")
else:
  print("Точка належить квадрату.")